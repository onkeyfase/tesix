void Buzzer_Beep(uint32_t frequency_hz, uint32_t duration_ms)
{
    uint32_t timer_clock = 16000000; // HSI = 16MHz, nessun PLL
    uint32_t arr = (timer_clock / frequency_hz) - 1;

    __HAL_TIM_SET_AUTORELOAD(&htim1, arr);
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, arr / 2); // 50% duty cycle
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
    HAL_Delay(duration_ms);
    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
}

